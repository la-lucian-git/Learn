integer = 8
string = "AlexL"

print("")
print("var = 8 --->", type(integer))
print("string = Alex --->", type(string))
print("len(string) --->", len(string))

# Slicing
print("string[0] --->", string[0])  # Value at first index
print("string[-1] --->", string[-1])  # Value at last index
print("string[0:4] --->", string[0:4])  # Up to but not including
print("string[:4] --->", string[:4])
print("string[1:] --->", string[1:])